<?php
// PSR - PHP Standard Recommendation é um conjunto de recomendações para padronizar o código PHP.
// As recomendações mais conhecidas são PSR-1, PSR-2 e PSR-4, essenciais para todos os projetos PHP.

// PSR-1 recomenda as seguintes regras para código PHP:
// - Use apenas as tags <?php e <?= para abrir e fechar o código PHP.
// - Utilize codificação UTF-8 sem BOM.
// - Organize o código em namespaces e classes.
// - Adote camelCase para nomes de métodos e classes.
// - Use constantes em letras maiúsculas, separadas por sublinhados.
// - Indente o código com 4 espaços.
// - Limite as linhas a no máximo 80 caracteres.
// - Insira uma única linha em branco no final do arquivo.

// PSR-2 complementa PSR-1 com as seguintes regras adicionais:
// - Mantenha a indentação com 4 espaços.
// - Continue limitando as linhas a no máximo 80 caracteres.
// - Garanta uma única linha em branco no final do arquivo.

// PSR-4 define padrões para autoloading de classes com as seguintes regras:
// - O diretório base do namespace deve ser especificado.
// - Os namespaces e classes devem seguir a estrutura de diretórios onde estão armazenados.
// - Cada namespace deve ter um diretório base correspondente.
// - Utilize o autoloader compatível com PSR-4 para carregar as classes automaticamente.
